<p>You will need to manually maintain all updates that we provide after purchase. The best practice is to follow the proper <a href="customization.php">Customization method</a> for a smooth update process. Every new update is considered a new and better version of the theme. Here are our recommended steps needed to update theme correctly:</p>
<ul>
	<li class="mb-2">Before you process the update take a backup of your project's current version</li>
	<li class="mb-2">Review themes <a href="changelog.php">changelog.</a> We provide a detailed changelog with a modified file list. You just need to update/replace those modified files to update the theme.</li>
	<li class="mb-2">Extract the latest version of the theme's <code>.zip</code> file and follow the below steps for individuals file/folder:
		<ul>
			<li class="mb-2"><b>SCSS:</b> If you followed <a href="customization.php">SCSS Method</a> for your customization then replace new updated <code>.scss</code> file with your current file(if you haven't changed anything in these files) and compile your changes by running <a href="gulp.php">gulp</a></li>
			<li class="mb-2"><b>CSS:</b> If you followed <a href="customization.php">CSS Method</a> for your customization then replace new updated <code>.css</code> file with your current file(if you haven't changed anything in these files)</li>
			<li class="mb-2"><b>JS:</b> Replace the latest <code>function.js</code> file with your current file</li>
			<li class="mb-2"><b>Vendor:</b> Replace the latest vendor folder with your current vendor folder and compile your changes by running gulp</li>
			<li class="mb-2"><b>HTML:</b> Please check notes in our changelog and update accordingly. You can compare your current HTML files VS updated latest files and replace/update code accordingly</li>
			<li class="mb-2"><b>Other:</b> We add a note for any other changes we have made except the above file/folders </li>
		</ul>
	</li>
</ul>
<p>You can always revert your updates from your backup files if something goes wrong.</p>
