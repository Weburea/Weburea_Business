    <script>
        document.addEventListener('DOMContentLoaded', function () {
            const videoContainer = document.getElementById('sticky-video-container');
            const video = document.getElementById('dynamic-video');
            const mainRow = videoContainer.closest('.row');
            // Target the title to start the animation earlier
            const triggerElement = document.querySelector('h1.display-5');

            if (videoContainer && video && mainRow && triggerElement) {
                // Ensure video transitions smoothly for transform
                video.style.transition = 'transform 0.1s linear, height 0.1s linear';

                window.addEventListener('scroll', function () {
                    const triggerRect = triggerElement.getBoundingClientRect();

                    // We want the animation to start early (when title moves) AND finish when we've scrolled 
                    // a good amount into the spacer, so the full video is visible for a while.
                    // Start when title is 200px from top
                    const startPoint = 200;
                    // End when title is 400px ABOVE top (scrolled past significantly into the spacer)
                    const endPoint = -400;

                    const totalDistance = startPoint - endPoint;

                    if (triggerRect.top <= startPoint) {
                        const progressRaw = (startPoint - triggerRect.top) / totalDistance;
                        const progress = Math.min(Math.max(progressRaw, 0), 1);

                        // 2. Expand Video
                        const initialWidth = videoContainer.offsetWidth; // Current width (~col-7)
                        const targetWidth = mainRow.offsetWidth; // Full row width (~col-12)

                        // Calculate scale
                        const maxScale = targetWidth / initialWidth;
                        const currentScale = 1 + ((maxScale - 1) * progress);

                        // Calculate Translation X (center it)
                        const totalTranslationX = -((targetWidth - initialWidth) / 2);
                        const translateX = totalTranslationX * progress;

                        // 3. Fix "Zoomed" look -> Aspect Ratio / Height
                        // Switch to 'contain' very early to avoid jump
                        if (progress > 0.05) {
                            video.style.objectFit = 'contain';
                        } else {
                            video.style.objectFit = 'cover';
                        }

                        video.style.transform = `translateX(${translateX}px) scale(${currentScale})`;

                    } else {
                        // Reset
                        video.style.transform = 'translateX(0) scale(1)';
                        video.style.objectFit = 'cover';
                    }
                });
            }
        });
    </script>
